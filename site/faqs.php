                            <strong class="brd-fs-clr">What's the Random Number Generation(RNG) algorithm in BitShares Play?</strong>
                            <div class="brd-fs-clr">
                                BitShares Play is using a provable distributed RNG algorithm from DPOS. “Distributed” means that the random number of one block is generated by the previous 101 delegates’ revealed secret, so as long as at least one of them are honest, the resulting random number is truly random. “Provable” means that they need to publish the hash of their secrets to the blockchain in the next round. The revealed secret’s hash must be the hash they published last time. They can not cheat by revealing selective favourable secret because the hash of the secret must be included in previous produced block.
